package pleat

import (
	"runtime"
)

type FieldKey string

// FieldMap allows customization of the key names for default fields.
type FieldMap map[FieldKey]string

func (f FieldMap) resolve(key FieldKey) string {
	if k, ok := f[key]; ok {
		return k
	}

	return string(key)
}

